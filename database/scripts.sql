Table Student:

```sql
create table Student(StudentID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, StudentName VARCHAR(70), Gender VARCHAR2(10),
MajorID NUMBER,
CONSTRAINT FK_MajorID_Student Foreign Key (MajorID) references Major(MajorID) on delete cascade,
DOB DATE NOT NULL,
POB VARCHAR2(70), PhoneNumber VARCHAR(15));
```

