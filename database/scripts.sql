table scores:

```sql
Create Table Scores(ScoreID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, StudentID NUMBER, SubjectID NUMBER, Score NUMBER(5,2),
CONSTRAINT chk_score_range CHECK (score >= 0 AND score <= 100)
);

