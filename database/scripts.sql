Table Subject:

```sql
Create table Subject(SubjectID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, SubjectName VARCHAR(60),
MajorID NUMBER, CONSTRAINT FK_MajorID Foreign Key (MajorID) references Major(MajorID) on delete cascade);
