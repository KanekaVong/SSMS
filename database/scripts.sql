SELECT 
    st.StudentID, 
    st.StudentName, 
    su.SubjectName AS Subject, 
    sc.score       AS Score
FROM 
    Student st 
JOIN 
    Scores sc ON st.StudentID = sc.studentid
JOIN 
    Subject su ON sc.subjectid = su.SubjectID
WHERE 
    st.StudentID = 4586;
    
//Funct to calculate Total Score and Overall Grade
SELECT
    st.StudentID,
    st.StudentName,
    COUNT(sc.score) AS TotalSubjects,
    SUM(sc.score) AS TotalScores,
    ROUND(AVG(sc.score), 2) AS Average,
    ROUND(AVG((sc.score) / 100) * 5, 2) AS GPA
FROM 
    Student st 
JOIN 
    Scores sc ON st.StudentID = sc.studentid
GROUP BY 
    st.StudentID, st.StudentName;
